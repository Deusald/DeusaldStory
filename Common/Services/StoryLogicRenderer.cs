using System;
using System.Collections.Generic;
using System.Linq;
using DeusaldLocalizerCommon;
using JetBrains.Annotations;

namespace DeusaldStoryCommon
{
    /// <summary>
    /// Resolves a logic node's inner content graph into plain rendered data — the title, optional subtitle, an
    /// optional icon (base64 PNG) and the ordered text blocks produced by walking the flow spine — for a given set
    /// of External Variable values. Host-agnostic so both the App preview, the Gamebook preview and (later) PDF
    /// export share one resolution; HTML sanitization is left to the caller (see <see cref="PreviewHtmlSanitizer"/>).
    /// </summary>
    [PublicAPI]
    public static class StoryLogicRenderer
    {
        /// <summary>Shared empty value map for flow-only walks (e.g. <see cref="ReachedExits"/>) that don't render text.</summary>
        private static readonly Dictionary<Guid, string> _EmptyValues = new();

        /// <summary>A logic node resolved to display text/icon for one combination of variable values.</summary>
        public sealed class RenderedLogic
        {
            public string       Title    { get; set; } = "";
            public string       Subtitle { get; set; } = "";
            public string?      IconData { get; set; }
            public List<string> Blocks   { get; set; } = new();

            /// <summary>The player choices offered at the end of the flow spine (empty when the node has no Choice node).</summary>
            public List<RenderedChoice> Choices { get; set; } = new();

            /// <summary>Player-facing storage instructions generated by this node's Register/Set/Unregister ops, in flow order.</summary>
            public List<StorageInstruction> Instructions { get; set; } = new();
        }

        /// <summary>One resolved choice — its player-facing text and the Exit point flow leaves through when taken.</summary>
        public sealed class RenderedChoice
        {
            public string Text { get; set; } = "";

            /// <summary>The Exit point this choice's flow-out is wired to (empty when the choice is not wired to an exit).</summary>
            public Guid ExitPointId { get; set; }
        }

        /// <summary>
        /// Resolves <paramref name="logic"/> against <paramref name="values"/> (story-variable id → value). When a
        /// referenced variable has no entry it falls back to the variable's first possible value.
        /// <paramref name="paper"/> selects the light side of any Light/Dark switch on the icon path.
        /// <paramref name="target"/> selects which branch App/Gamebook splitter nodes take (App preview vs Gamebook).
        /// </summary>
        public static RenderedLogic Render(
            StoryProject project, LocProject? localization, StoryLogicNode logic,
            IReadOnlyDictionary<Guid, string> values, bool paper, StoryRenderTarget target = StoryRenderTarget.App)
        {
            WalkSpine(project, localization, logic, values, target, out List<string> blocks, out List<RenderedChoice> choices, out _);
            return new RenderedLogic
            {
                Title        = ResolveText(project, localization, logic, values, FromPointInto(logic, logic.TitleIn.Id),    target, 0),
                Subtitle     = ResolveText(project, localization, logic, values, FromPointInto(logic, logic.SubtitleIn.Id), target, 0),
                IconData     = ResolveIconImage(project, logic, FromPointInto(logic, logic.IconIn.Id), paper, 0),
                Blocks       = blocks,
                Choices      = choices,
                Instructions = StoryStorageInstructions.For(project, localization, logic, target, values)
            };
        }

        /// <summary>The choices offered at the end of <paramref name="logic"/>'s flow spine (empty when it has no Choice node).</summary>
        public static List<RenderedChoice> Choices(
            StoryProject project, LocProject? localization, StoryLogicNode logic,
            IReadOnlyDictionary<Guid, string> values, StoryRenderTarget target = StoryRenderTarget.App)
        {
            WalkSpine(project, localization, logic, values, target, out _, out List<RenderedChoice> choices, out _);
            return choices;
        }

        /// <summary>
        /// The exit point(s) the flow spine flows into for <paramref name="target"/>: a linear spine reaches exactly
        /// one exit, and an App/Gamebook flow splitter routes each medium to its own — so the App preview and the
        /// Gamebook only offer the continuations their medium actually flows into. Empty when the spine ends in a
        /// Choice (whose options carry their own exits), at an unconnected flow-splitter branch, or a dead end.
        /// Meaningful only for <see cref="StoryLogicExitMode.SeparatePaths"/> nodes; in SingleSelection mode the exits
        /// are Selection labels the spine never flows into (flow leaves through the single Selection flow-out instead).
        /// </summary>
        public static List<Guid> ReachedExits(StoryProject project, StoryLogicNode logic, StoryRenderTarget target = StoryRenderTarget.App)
        {
            WalkSpine(project, null, logic, _EmptyValues, target, out _, out _, out List<Guid> reached);
            return reached;
        }

        /// <summary>
        /// Resolves the text wired into <paramref name="toPoint"/> (a Localization / SmartFormat / splitter output),
        /// for flow-only callers that carry no External Variable values — e.g. the storage-instruction generator
        /// reading a Register/Set node's Instruction port. Empty when nothing is connected.
        /// When <paramref name="slot"/> is supplied the port's text is SmartFormatted with a <c>{slot}</c> placeholder
        /// bound to it — the storage slot the instruction writes to, as its label (e.g. <c>SA</c>) or the styled
        /// <see cref="PreviewHtmlSanitizer.SlotTag"/> pill markup. The value is merged into any SmartFormat node in the
        /// path (so it resolves alongside the node's other variables in one pass) and, for a plain Localization
        /// instruction carrying only <c>{slot}</c>, a final pass fills it in.
        /// </summary>
        public static string ResolvePortText(
            StoryProject project, LocProject? localization, StoryLogicNode logic, Guid toPoint,
            StoryRenderTarget target = StoryRenderTarget.App, string? slot = null)
        {
            Dictionary<string, object>? extra = slot is null
                ? null
                : new Dictionary<string, object>(StringComparer.OrdinalIgnoreCase) { ["slot"] = slot };
            string text = ResolveText(project, localization, logic, _EmptyValues, FromPointInto(logic, toPoint), target, 0, extra);
            // A plain Localization instruction ("…in slot {slot}") never hit a SmartFormat node above, so bind {slot}
            // now; when it was resolved through a SmartFormat node {slot} is already gone and this is a no-op.
            return extra is null ? text : StoryConditionPreview.Render(text, extra, out _);
        }

        // ── Flow-spine walk ──────────────────────────────────────────────────────

        /// <summary>
        /// Walks the flow spine from the Entry, collecting the ordered text <paramref name="blocks"/> rendered by
        /// FlowText nodes and, on reaching a Choice node, the resolved <paramref name="choices"/>. Register / Set /
        /// Unregister / Set-External nodes on the spine are passed straight through (they render no text). A Choice
        /// node terminates the spine — its branches leave the node through exits.
        /// </summary>
        private static void WalkSpine(
            StoryProject project, LocProject? localization, StoryLogicNode logic, IReadOnlyDictionary<Guid, string> values,
            StoryRenderTarget renderTarget, out List<string> blocks, out List<RenderedChoice> choices, out List<Guid> reachedExits)
        {
            blocks       = new();
            choices      = new();
            reachedExits = new();
            HashSet<Guid> exitIds = new(logic.ExitPoints.Select(e => e.Id));
            Guid target = FlowTargetOf(logic, logic.EntryPoint.Id);

            for (int guard = 0; guard < 256; ++guard)
            {
                if (logic.FlowTextNodes.Find(n => n.FlowIn.Id == target) is StoryFlowTextNode ft)
                {
                    blocks.Add(ResolveText(project, localization, logic, values, FromPointInto(logic, ft.TextIn.Id), renderTarget, 0));
                    target = FlowTargetOf(logic, ft.FlowOut.Id);
                }
                else if (logic.ChoiceNodes.Find(n => n.FlowIn.Id == target) is StoryChoiceNode choice)
                {
                    foreach (StoryChoiceOption option in choice.Options)
                        choices.Add(new RenderedChoice
                        {
                            Text        = ResolveText(project, localization, logic, values, FromPointInto(logic, option.TextIn.Id), renderTarget, 0),
                            ExitPointId = FlowTargetOf(logic, option.FlowOut.Id)
                        });
                    break; // a choice branches out to exits — the spine ends here
                }
                else if (logic.AppGamebookFlowSplitterNodes.Find(n => n.FlowIn.Id == target) is StoryAppGamebookFlowSplitterNode fs)
                    target = FlowTargetOf(logic, renderTarget == StoryRenderTarget.App ? fs.AppFlowOut.Id : fs.GamebookFlowOut.Id);
                else if (logic.ConditionNodes.Find(n => n.FlowIn.Id == target) is StoryConditionNode cond)
                    target = FlowTargetOf(logic, StoryLogicFlow.ConditionBranch(project, logic, cond, values, renderTarget));
                else if (logic.SetExternalVariableNodes.Find(n => n.FlowIn.Id == target) is StorySetExternalVariableNode se)
                    target = FlowTargetOf(logic, se.FlowOut.Id);
                else if (logic.RegisterVariableNodes.Find(n => n.FlowIn.Id == target) is StoryRegisterVariableNode reg)
                    target = FlowTargetOf(logic, reg.FlowOut.Id);
                else if (logic.SetVariableNodes.Find(n => n.FlowIn.Id == target) is StorySetVariableNode set)
                    target = FlowTargetOf(logic, set.FlowOut.Id);
                else if (logic.UnregisterVariableNodes.Find(n => n.FlowIn.Id == target) is StoryUnregisterVariableNode unreg)
                    target = FlowTargetOf(logic, unreg.FlowOut.Id);
                else
                {
                    if (exitIds.Contains(target)) reachedExits.Add(target); // the spine flowed into one of the node's exits
                    break; // reached an Exit or a leaf input
                }
            }
        }

        private static Guid FlowTargetOf(StoryLogicNode logic, Guid fromPoint) =>
            logic.ContentConnections.Find(c => c.FromPoint == fromPoint)?.ToPoint ?? Guid.Empty;

        // ── Text resolution ──────────────────────────────────────────────────────

        private static string ResolveText(
            StoryProject project, LocProject? localization, StoryLogicNode logic,
            IReadOnlyDictionary<Guid, string> values, Guid fromPoint, StoryRenderTarget target, int depth,
            IReadOnlyDictionary<string, object>? extraValues = null)
        {
            if (fromPoint == Guid.Empty || depth > 8) return "";

            if (logic.LocalizationNodes.Find(n => n.OutPoint.Id == fromPoint) is StoryLocalizationNode locNode)
                return LocalizedText(localization, locNode.SelectedKeyId);

            if (logic.AppGamebookTextSplitterNodes.Find(n => n.OutPoint.Id == fromPoint) is StoryAppGamebookTextSplitterNode ts)
            {
                // Emit the branch for the medium being rendered; an unconnected branch resolves to empty text.
                Guid input = target == StoryRenderTarget.App ? ts.AppTextIn.Id : ts.GamebookTextIn.Id;
                return ResolveText(project, localization, logic, values, FromPointInto(logic, input), target, depth + 1, extraValues);
            }

            if (logic.SmartFormatNodes.Find(n => n.OutPoint.Id == fromPoint) is StorySmartFormatNode sf)
            {
                string format = ResolveText(project, localization, logic, values, FromPointInto(logic, sf.LocalizationIn.Id), target, depth + 1, extraValues);

                Dictionary<string, object> vals = new(StringComparer.OrdinalIgnoreCase);
                // Caller-supplied placeholders (e.g. a Set/Register instruction's {slot}) resolve alongside the node's
                // own variables in this one pass — SmartFormat throws on any unknown token, so they must share a dict.
                if (extraValues is not null)
                    foreach (KeyValuePair<string, object> kv in extraValues) vals[kv.Key] = kv.Value;
                foreach (StoryConnection c in logic.ContentConnections.Where(c => c.ToPoint == sf.VariablesIn.Id))
                {
                    if (logic.ExternalVariableNodes.Find(n => n.OutPoint.Id == c.FromPoint) is StoryExternalVariableNode ev
                        && Variable(project, ev.SelectedVariableId) is StoryVariable v && !string.IsNullOrWhiteSpace(v.Name))
                        vals[v.Name] = StoryBuiltInVariables.IsBuiltIn(v.Id)
                            ? StoryBuiltInVariables.ValueFor(target) // the medium variable follows the render target, not the preview values
                            : PreviewValue(v, values);
                    else if (c.FromPoint == logic.PrevExitVariable.OutPoint.Id && logic.AcceptExitVariable)
                    {
                        // The upstream Selection value for this section (keyed by the Prev Exit Variable node's id).
                        string name = string.IsNullOrWhiteSpace(logic.PrevExitVariable.VariableName) ? "Selection" : logic.PrevExitVariable.VariableName;
                        vals[name] = values.TryGetValue(logic.PrevExitVariable.Id, out string? sel) ? sel : "";
                    }
                }

                return StoryConditionPreview.Render(format, vals, out _);
            }

            return "";
        }

        /// <summary>The main-language text of the localization key <paramref name="keyId"/> (empty when unavailable).</summary>
        public static string LocalizedText(LocProject? localization, Guid keyId)
        {
            LocLocalizationKey? key = localization?.Keys.Find(k => k.Id == keyId);
            if (key is null) return "";

            string? main = localization!.Metadata.MainLanguageId;
            if (string.IsNullOrEmpty(main)) return "";
            return key.Translations.Find(t => t.LanguageId == main)?.Text ?? "";
        }

        private static string PreviewValue(StoryVariable v, IReadOnlyDictionary<Guid, string> values) =>
            values.TryGetValue(v.Id, out string? val) ? val : v.PossibleValues.FirstOrDefault() ?? "";

        private static StoryVariable? Variable(StoryProject project, Guid id) =>
            StoryBuiltInVariables.Find(id)
            ?? (id != Guid.Empty && project.Variables.TryGetValue(id, out StoryVariable? v) ? v : null);

        // ── Icon resolution ──────────────────────────────────────────────────────

        private static string? ResolveIconImage(StoryProject project, StoryLogicNode logic, Guid fromPoint, bool paper, int depth)
        {
            if (fromPoint == Guid.Empty || depth > 8) return null;

            if (logic.IconNodes.Find(n => n.OutPoint.Id == fromPoint) is StoryIconNode icon)
                return project.Images.TryGetValue(icon.SelectedImageId, out StoryImage? img) ? img.Data : null;

            if (logic.LightDarkSwitchNodes.Find(n => n.OutPoint.Id == fromPoint) is StoryLightDarkSwitchNode sw)
            {
                Guid input = paper ? sw.LightIn.Id : sw.DarkIn.Id;
                return ResolveIconImage(project, logic, FromPointInto(logic, input), paper, depth + 1);
            }

            return null;
        }

        /// <summary>The output point wired into <paramref name="toPoint"/>, or empty when nothing is connected.</summary>
        private static Guid FromPointInto(StoryLogicNode logic, Guid toPoint) =>
            logic.ContentConnections.Find(c => c.ToPoint == toPoint)?.FromPoint ?? Guid.Empty;
    }
}
